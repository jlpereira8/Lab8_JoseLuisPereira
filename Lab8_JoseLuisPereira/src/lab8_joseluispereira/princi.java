    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        //String nombre, String empresa, String contra, String boss, double salario, String puesto
        if(bt_gg.isEnabled()){
        double Salario = Double.parseDouble(gg_salario.getText());
        empleados temp = new empleados(gg_nombre.getText(), gg_empresa.getText(), gg_contra.getText(), "nadie", Salario, "Gerente General");
        emple.add(temp);
        DefaultTreeModel m = (DefaultTreeModel) jt_arbol.getModel();
        raiz = (DefaultMutableTreeNode) m.getRoot();
        nodo_carro = new DefaultMutableTreeNode(
                new empleados(gg_nombre.getText(), gg_empresa.getText(), gg_contra.getText(), "nadie", Salario, "Gerente General"));
        m.setRoot(nodo_carro);
        m.reload();
        bt_gg.setEnabled(false);
        crear_gernete.dispose();
        gg_nombre2.setEnabled(true); 
        gg_empresa2.setEnabled(true); gg_contra2.setEnabled(true); wombo_combo.setEnabled(true);gg_salario2.setEnabled(true);bt_pieza.setEnabled(true);gg_id2.setEnabled(true);
        JOptionPane.showMessageDialog(this, "Recuerda que luego de creado el gerente general no podras crear otro");
        }
    }//GEN-LAST:event_jButton4MouseClicked

