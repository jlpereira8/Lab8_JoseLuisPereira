    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        //String nombre, String empresa, String contra, String boss, double salario, String puesto
        if (jl_empleados.getSelectedIndex() >= 0) {// verificar si teiene una persona seleccionada
            DefaultTreeModel modeloARBOL = (DefaultTreeModel) jt_arbol.getModel();
            raiz = (DefaultMutableTreeNode) modeloARBOL.getRoot();
            //obtenera la persona a guardar
            DefaultListModel modeloLISTA = (DefaultListModel) jl_empleados.getModel();
            String nombre,empresa,contra,boss,puesto;
            nombre = ((empleados) modeloLISTA.get(jl_empleados.getSelectedIndex())).getNombre();
            empresa=((empleados) modeloLISTA.get(jl_empleados.getSelectedIndex())).getEmpresa();
            contra=((empleados) modeloLISTA.get(jl_empleados.getSelectedIndex())).getContra();
            boss=((empleados) modeloLISTA.get(jl_empleados.getSelectedIndex())).getBoss();
            double salario=((empleados) modeloLISTA.get(jl_empleados.getSelectedIndex())).getSalario();
            puesto=((empleados) modeloLISTA.get(jl_empleados.getSelectedIndex())).getPuesto();
            int centinela = -1;
            for (int i = 0; i < raiz.getChildCount(); i++) {
                if (raiz.getChildAt(i).toString().equals(wombo_combo.getSelectedItem().toString())) {
                    DefaultMutableTreeNode p = new DefaultMutableTreeNode(
                                 new empleados(gg_nombre2.getText(), gg_empresa2.getText(), gg_contra2.getText(), null, salario, wombo_combo.getSelectedItem().toString()));
                    ((DefaultMutableTreeNode) raiz.getChildAt(i)).add(p);
                    centinela = 1;
                }
               
            } if (centinela == -1) {
                DefaultMutableTreeNode n = new DefaultMutableTreeNode(wombo_combo.getSelectedItem().toString());
                DefaultMutableTreeNode p = new DefaultMutableTreeNode(new empleados(gg_nombre2.getText(), gg_empresa2.getText(), gg_contra2.getText(), null, salario, wombo_combo.getSelectedItem().toString()));
                n.add(p);
                raiz.add(n);
            }
            modeloARBOL.reload();
        } else {
            JOptionPane.showMessageDialog(this, "No hay piezas seleccionadas");
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jt_arbolMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jt_arbolMouseClicked
        // TODO add your handling code here:
        if (evt.isMetaDown()) {
            int row = jt_arbol.getClosestRowForLocation(evt.getX(), evt.getY());
            jt_arbol.setSelectionRow(row);
            Object v1 = jt_arbol.getSelectionPath().getPathComponent(row);
            nodo_pieza = (DefaultMutableTreeNode) v1;
            if (nodo_pieza.getUserObject() instanceof empleados) {
                pieza_selccionada = (empleados) nodo_pieza.getUserObject();
                popup_borrar.show(evt.getComponent(), evt.getX(), evt.getY());
            }
        }
        
    }//GEN-LAST:event_jt_arbolMouseClicked

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(this, "Seguro de Eliminar ? ", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == JOptionPane.OK_OPTION) {
            DefaultTreeModel m = (DefaultTreeModel) jt_arbol.getModel();
            m.removeNodeFromParent(nodo_pieza);
            m.reload();
            
        }
    }//GEN-LAST:event_EliminarActionPerformed

